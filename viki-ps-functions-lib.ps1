function BLUE($msg){write-host $msg -ForegroundColor Blue}
function CYAN($msg){write-host $msg -ForegroundColor Cyan}
function DARKBLUE($msg){write-host $msg -ForegroundColor DarkBlue}
function DARKRED($msg){write-host $msg -ForegroundColor DarkRed}
function DARKYELLOW($msg){write-host $msg -ForegroundColor DarkYellow}
function WHITE($msg){write-host $msg -ForegroundColor White}
function YELLOW($msg){write-host $msg -ForegroundColor Yellow}
